package util;

public class Calculate {
		public int  add( int a,int b){
			int c=a+b;
			return c;
			
		}
		public int  multiply( int a,int b){
			int c=a*b;
			return c;
			
		}
		public int  substract( int a,int b){
			int c=a-b;
			return c;
			
		}
		public int  divide( int a,int b){
			int c=a/b;
			return c;
			
		}
}
